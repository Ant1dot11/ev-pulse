from gemini_client import generate


def create_post(title, article_text, source):

   prompt = f"""
   Ти — редактор українського медіа EV Pulse.

Напиши новину так, ніби її написав журналіст автомобільного видання, а не штучний інтелект.

====================

ЗАГОЛОВОК

{title}

====================

СТАТТЯ

{article_text}

====================

ДЖЕРЕЛО

{source}

====================

ПРАВИЛА

• Пиши українською мовою.

• Не використовуй Markdown (**).

• Не використовуй фрази:
- "Привіт EV-спільното"
- "Як головний редактор"
- "На мою думку"
- "Я вважаю"

• Не вигадуй жодного факту.

• Якщо джерело висловлює власну думку або припущення — обов'язково покажи, що це саме позиція автора статті, а не встановлений факт.

Наприклад:

❌ "Американські виробники не можуть конкурувати."

✅ "Автори CleanTechnica вважають, що американські виробники побоюються конкуренції."

====================

СТРУКТУРА

⚡ Короткий заголовок.

1–2 речення вступу.

Далі:

🔹 Що сталося

🔹 Чому це важливо

💬 EV Pulse

====================

Блок "EV Pulse"

Пиши дуже природно.

Не використовуй шаблонні фрази типу:

"ця ситуація висвітлює"

"це піднімає питання"

"варто зазначити"

"безумовно"

"можна зробити висновок"

Пиши як журналіст.

Приклад:

"Якщо закон ухвалять, США фактично закриють свій ринок для китайських електромобілів. Для самих виробників це навряд чи стане критичною проблемою, адже вони активно нарощують продажі в Європі, Південній Америці та інших регіонах."

====================

Оформлення

Абзаци по 2–3 речення.

Без величезних полотен тексту.

Без повторів.

Обсяг 900–1300 символів.

Наприкінці:

Джерело:
{source}

#EVPulse #Електромобілі
"""
   return generate(prompt)