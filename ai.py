from gemini_client import generate


def create_post(title, article_text, source):
    prompt = f"""
Ти — головний редактор українського медіа EV Pulse.

Твоя задача — написати новину так, ніби її підготував досвідчений журналіст автомобільного видання.
Не повинно бути відчуття, що текст написав штучний інтелект.

====================
ЗАГОЛОВОК
{title}

====================
СТАТТЯ
{article_text}

====================
ДЖЕРЕЛО
{source}

СУВОРІ ПРАВИЛА (дотримуйся ВСІХ без винятку):

1. Пиши лише українською мовою.
2. Не вигадуй фактів.
3. НІКОЛИ не використовуй Markdown-розмітку: жодних символів **, жодних #, жодного іншого форматування (крім хештегів у самому кінці, як показано у форматі нижче).
4. Якщо джерело містить припущення — позначай це як думку автора.
5. Обсяг основного тексту (без блоку "Джерело:" і хештегів) — СУВОРО 700–950 символів. Це жорсткий ліміт.
6. Використовуй ТОЧНО таку структуру і ТОЧНО такі заголовки блоків, як у форматі нижче. Не змінюй формулювання заголовків, не пропускай блоки, не додавай нових.

ФОРМАТ ВІДПОВІДІ (заповни своїм текстом замість [...], решту скопіюй один в один):

⚡️ [Короткий заголовок новини]

[Перший абзац на 1-2 речення, який одразу зацікавить читача]

🔹 Що сталося

[1-2 абзаци по 1-2 речення, факти зі статті]

🔹 Чому це важливо

[1-2 абзаци по 1-2 речення, контекст і значення]

💬 Думка EV Pulse

[1-2 абзаци по 1-2 речення, аналітична думка]

Джерело:
{source}

#EVPulse #Електромобілі

Поверни ЛИШЕ готовий текст поста, без жодних власних коментарів чи пояснень.
"""
    return generate(prompt)